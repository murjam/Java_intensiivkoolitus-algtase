package paev1;

public class Yl13_KymmeKuniYks {
	public static void main(String[] args) {
		
		//Trükkida ekraanile arvud 1 kuni 10.
		for (int alates = 1; alates <= 10; alates++) {
			System.out.print(alates + " ");
		}
		
		System.out.println();
		
		//Trükkida ekraanile arvud 10 kuni 1.
		for (int alates = 10; alates > 0; alates--) {
			System.out.print(alates + " ");
		}
	}
}
